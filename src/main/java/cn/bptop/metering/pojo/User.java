package cn.bptop.metering.pojo;

public class User
{
    private String id;
    private String ddId;
    private String ddName;

    public String getId()
    {
        return id;
    }

    public void setId(String id)
    {
        this.id = id;
    }

    public String getDdId()
    {
        return ddId;
    }

    public void setDdUserId(String ddId)
    {
        this.ddId = ddId;
    }

    public String getDdName()
    {
        return ddName;
    }

    public void setDdName(String ddName)
    {
        this.ddName = ddName;
    }
}
